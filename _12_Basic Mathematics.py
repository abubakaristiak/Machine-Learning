import numpy as np
# Increment/ Addition operator:
n1 = np.array([10,20,30])
n1 = n1+1
print(n1)

# Substraction operator:
n2 = np.array([15,25,35])
n2 = n2-1
print(n2)

# Multiplication operator:
n3 = np.array([2,4,6])
n3 = n3*2
print(n3)

# Divition operator:
n4 = np.array([15,25,35])
n4 = n4/5
print(n4)

